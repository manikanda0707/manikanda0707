package SampleJavaProgram;

public class NumberSwap3rd {

	public static void main(String[] args) {
		
		int me=5000;
		int he=10000;
		int temp;
		System.out.println(" before" );
		System.out.println("my salary" +me );
		System.out.println("he salary" +he );
		temp=me;
		me=he;
		he=temp;
		System.out.println(" after"  );
		System.out.println("my salary" +me );
		System.out.println("he salary" +he );
		
	}
}
